	public String getLngLat(String address) {
        StringBuffer json = new StringBuffer();
        try {
            URL u = new URL("http://restapi.amap.com/v3/geocode/geo?address="+address+"&output=JSON&key=d8440a22fb3fc04b72a61aa6b51902a2");
            URLConnection yc = u.openConnection();
            //读取返回的数据
            BufferedReader in = new BufferedReader(new InputStreamReader(yc.getInputStream(),"UTF-8"));
            String inputline = null;
            while((inputline=in.readLine())!=null){
                json.append(inputline);
            }
        in.close();
        } catch (Exception e) {
             e.printStackTrace();
         }
        String jsonStr=json.toString();
//        JSONObject jsonObject = JSONObject.fromObject(jsonStr);
        JSONObject jsonObject = JSONObject.parseObject(jsonStr);
        
//        System.out.println(jsonObject);
        
        //判断输入的位置点是否存在
        if(jsonObject !=null) {
        	if(jsonObject.getJSONArray("geocodes") == null) {
        		return "";
        	}
        	if(jsonObject.getJSONArray("geocodes").size()>0) {
        		return jsonObject.getJSONArray("geocodes").getJSONObject(0).get("location").toString();
        	}
        }
        else {
            return "";
        }
		return jsonStr;
    }
	
